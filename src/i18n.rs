// ─── Macro to define translations concisely ─────────────────────────
// Fields not specified fall back to English.
macro_rules! lang {
    ($name:ident { $($field:ident : $val:expr),* $(,)? }) => {
        static $name: Strings = Strings {
            $($field: $val,)*
            ..EN
        };
    };
}

